package com.llbt.cms.entity;

import com.llbt.cms.common.BaseEntity;

/**
 * Department实体对象
 * 
 * @author 修改成自己的名字
 * @version 1.0
 */
public class Department extends BaseEntity {
	private static final long serialVersionUID = -329483485528070835L;
	/** 上级机构ID */
	private String parentId;
	/*上级部分名称*/
	private String parentName;
	/** 机构级别:1为最高,依上下级关系递减递增 */
	private Integer deptLevel;
	/** 机构名称 */
	private String deptName;
	/** 机构代码 */
	private String deptCode;
	/** 机构描述 */
	private String deptDesc;
	/** 删除状态:0未删除 1已删除 */
	private int isDelete;
	/** 机构状态:1启动 0停用 */
	private String deptState;

	/**
	 * @return the parentId
	 */
	public String getParentId() {
		return this.parentId;
	}

	/**
	 * @param parentId
	 *            the parentId to set
	 */
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}

	public String getDeptName() {
		return this.deptName;
	}

	public Integer getDeptLevel() {
		return deptLevel;
	}

	public void setDeptLevel(Integer deptLevel) {
		this.deptLevel = deptLevel;
	}

	/**
	 * @param deptName
	 *            the deptName to set
	 */
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	/**
	 * @return the deptCode
	 */
	public String getDeptCode() {
		return this.deptCode;
	}

	/**
	 * @param deptCode
	 *            the deptCode to set
	 */
	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}

	/**
	 * @return the deptDesc
	 */
	public String getDeptDesc() {
		return this.deptDesc;
	}

	/**
	 * @param deptDesc
	 *            the deptDesc to set
	 */
	public void setDeptDesc(String deptDesc) {
		this.deptDesc = deptDesc;
	}

	/**
	 * @return the isDelete
	 */
	public int getIsDelete() {
		return this.isDelete;
	}

	/**
	 * @param isDelete
	 *            the isDelete to set
	 */
	public void setIsDelete(int isDelete) {
		this.isDelete = isDelete;
	}

	/**
	 * @return the deptState
	 */
	public String getDeptState() {
		return this.deptState;
	}

	/**
	 * @param deptState
	 *            the deptState to set
	 */
	public void setDeptState(String deptState) {
		this.deptState = deptState;
	}

	public String getParentName() {
		return parentName;
	}

	public void setParentName(String parentName) {
		this.parentName = parentName;
	}
}
