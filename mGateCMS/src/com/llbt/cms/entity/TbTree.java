package com.llbt.cms.entity;

import java.util.ArrayList;
import java.util.List;

import com.llbt.cms.common.BaseEntity;

public class TbTree extends BaseEntity {
	private String cid;
	private String cname;
	private String pid;
	private List<TbTree> nodes = new ArrayList<TbTree>();
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public List<TbTree> getNodes() {
		return nodes;
	}
	public void setNodes(List<TbTree> nodes) {
		this.nodes = nodes;
	}
	
}
