package com.llbt.cms.entity;

import com.llbt.cms.common.BaseEntity;

/**
 * SystemParam实体对象
 * 
 * @author 修改成自己的名字
 * @version 1.0
 */
public class SystemParam extends BaseEntity {
	private static final long serialVersionUID = -2956829376397501541L;
	/** 参数标题名 */
	private String paramTitle;
	/** 参数键key */
	private String paramKey;
	/** 参数值 */
	private String paramValue;
	/** 参数描述 */
	private String paramDesc;
	/** 删除状态:0未删除 1已删除 */
	private int isDelete;

	/**
	 * @return the paramTitle
	 */
	public String getParamTitle() {
		return this.paramTitle;
	}

	/**
	 * @param paramTitle
	 *            the paramTitle to set
	 */
	public void setParamTitle(String paramTitle) {
		this.paramTitle = paramTitle;
	}

	/**
	 * @return the paramKey
	 */
	public String getParamKey() {
		return this.paramKey;
	}

	/**
	 * @param paramKey
	 *            the paramKey to set
	 */
	public void setParamKey(String paramKey) {
		this.paramKey = paramKey;
	}

	/**
	 * @return the paramValue
	 */
	public String getParamValue() {
		return this.paramValue;
	}

	/**
	 * @param paramValue
	 *            the paramValue to set
	 */
	public void setParamValue(String paramValue) {
		this.paramValue = paramValue;
	}

	/**
	 * @return the paramDesc
	 */
	public String getParamDesc() {
		return this.paramDesc;
	}

	/**
	 * @param paramDesc
	 *            the paramDesc to set
	 */
	public void setParamDesc(String paramDesc) {
		this.paramDesc = paramDesc;
	}

	/**
	 * @return the isDelete
	 */
	public int getIsDelete() {
		return this.isDelete;
	}

	/**
	 * @param isDelete
	 *            the isDelete to set
	 */
	public void setIsDelete(int isDelete) {
		this.isDelete = isDelete;
	}
}
